# Αυτόματη εύρεση της σημερινής ημέρας
days_gr = ["Δευτέρα", "Τρίτη", "Τετάρτη", "Πέμπτη", "Παρασκευή", "Σάββατο", "Κυριακή"]
today_index = datetime.datetime.today().weekday()
today_str = days_gr[today_index]

# Δημιουργία τριών καρτελών (tabs) για καθαρό περιβάλλον
tab1, tab2, tab3 = st.tabs(["Ημέρα", "Ψώνια", "Σημειώσεις"])

with tab1:
    st.subheader(f"Γεύματα για {today_str}")
    
    # Έξυπνες ειδοποιήσεις ανάλογα με την ώρα
    current_hour = datetime.datetime.now().hour
    if 16 <= current_hour <= 18:
        st.info("Υπενθύμιση: Ήρθε η ώρα για το απογευματινό σνακ.")
    elif current_hour >= 20:
        st.warning("Υπενθύμιση: Μην ξεχάσεις να βγάλεις το φαγητό από την κατάψυξη για αύριο.")

    # Αποθήκευση επιλογών στα κουτάκια
    if "checked_meals" not in st.session_state:
        st.session_state.checked_meals = {}

    # Εμφάνιση γευμάτων και εναλλακτικών
    for meal_time, meal_data in diet_plan[today_str].items():
        key = f"{today_str}_{meal_time}"
        if key not in st.session_state.checked_meals:
            st.session_state.checked_meals[key] = False
        
        # Το βασικό γεύμα σαν κουτάκι επιλογής
        checked = st.checkbox(f"{meal_time}: {meal_data['βασικό']}", value=st.session_state.checked_meals[key])
        st.session_state.checked_meals[key] = checked
        
        # Κρυφό μενού με την εναλλακτική
        with st.expander("Δες εναλλακτική"):
            st.write(meal_data["εναλλακτική"])
            
    # Υπολογισμός και εμφάνιση προόδου
    total = len(diet_plan[today_str])
    completed = sum(1 for time in diet_plan[today_str].keys() if st.session_state.checked_meals.get(f"{today_str}_{time}", False))
    
    st.write("")
    st.write(f"Πρόοδος ημέρας: {completed} από τα {total} γεύματα")
    if total > 0:
        st.progress(completed / total)

